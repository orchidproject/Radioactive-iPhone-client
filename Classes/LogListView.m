#pragma mark - Table view delegate


- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    // Navigation logic may go here. Create and push another view controller.
    
     LogView *detailedViewController = [[LogView alloc] initWithNibName:@"LogView" bundle:nil];
     
    NSManagedObject *obj= [[lqAppDelegate getLogs] objectAtIndex:indexPath.row];
    NSString *text= [obj valueForKey:@"content"];
    
    
    [self presentModalViewController:detailedViewController animated:YES];
    detailedViewController.logText.text=text;
     [detailedViewController release];
     
}



